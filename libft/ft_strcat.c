#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*dest;

	dest = s1;
	dest += ft_strlen(s1);
	while (*s2)
		*dest++ = *s2++;
	*dest = '\0';
	return (s1);
}
